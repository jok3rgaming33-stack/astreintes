/**
 * Returns the set of `nom` values (from PEOPLE) that are on-call on the given date.
 * Uses local date (France) — pass `new Date()` for today.
 */
export function getOnCallNoms(date: Date): Set<string> {
  const month = date.getMonth() + 1; // 1-12
  const day = date.getDate();

  const row = RAW.find((r) => r[0] === month && r[1] === day);
  if (!row) return new Set();

  const result = new Set<string>();
  for (let col = 0; col < COL_TO_NOM.length; col++) {
    const nom = COL_TO_NOM[col];
    if (nom && row[col + 2] === 1) {
      result.add(nom);
    }
  }
  return result;
}
